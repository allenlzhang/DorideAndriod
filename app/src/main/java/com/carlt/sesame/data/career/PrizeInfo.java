
package com.carlt.sesame.data.career;

public class PrizeInfo {
    // ID
    private String id;

    // 名称
    private String name;

    // 奖品图片
    private String iconUrl;

    // 描述
    private String description;

    // 使用说明
    private String usedescription;

    // 优惠券密码
    private String code;

    // 到期时间
    private String expiredate;

    // 数据创建时间
    private String createtime;

    // 4S店名称
    private String dealer_name;

    // 4S店信息
    private String dealer_info;

    // 4S店电话
    private String dealer_tel;

    private String shareLink;

    private String shareTitle;

    private String shareText;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIconUrl() {
        return iconUrl;
    }

    public void setIconUrl(String iconUrl) {
        this.iconUrl = iconUrl;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getExpiredate() {
        return expiredate;
    }

    public void setExpiredate(String expiredate) {
        this.expiredate = expiredate;
    }

    public String getCreatetime() {
        return createtime;
    }

    public void setCreatetime(String createtime) {
        this.createtime = createtime;
    }

    public String getDealer_name() {
        return dealer_name;
    }

    public void setDealer_name(String dealer_name) {
        this.dealer_name = dealer_name;
    }

    public String getDealer_info() {
        return dealer_info;
    }

    public void setDealer_info(String dealer_info) {
        this.dealer_info = dealer_info;
    }

    public String getDealer_tel() {
        return dealer_tel;
    }

    public void setDealer_tel(String dealer_tel) {
        this.dealer_tel = dealer_tel;
    }

    public String getUsedescription() {
        return usedescription;
    }

    public void setUsedescription(String usedescription) {
        this.usedescription = usedescription;
    }

    public String getShareLink() {
        return shareLink;
    }

    public void setShareLink(String shareLink) {
        this.shareLink = shareLink;
    }

    public String getShareTitle() {
        return shareTitle;
    }

    public void setShareTitle(String shareTitle) {
        this.shareTitle = shareTitle;
    }

    public String getShareText() {
        return shareText;
    }

    public void setShareText(String shareText) {
        this.shareText = shareText;
    }

}
